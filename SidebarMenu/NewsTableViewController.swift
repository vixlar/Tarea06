    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("Cell", forIndexPath: indexPath) as! NewsTableViewCell

        // Configure the cell...
        if indexPath.row == 0 {
            cell.postImageView.image = UIImage(named: "watchkit-intro")
            cell.postTitleLabel.text = "Inauguración del Edificio de Salud de la Universidad Modelo Mérida"
            cell.authorLabel.text = "Universidad Modelo"
            cell.authorImageView.image = UIImage(named: "appcoda-300")

        } else if indexPath.row == 1 {
            cell.postImageView.image = UIImage(named: "custom-segue-featured-1024")
            cell.postTitleLabel.text = "Inauguración del Edificio de Secundaria de la Universidad Modelo Valladolid"
            cell.authorLabel.text = "Escuela Modelo"
            cell.authorImageView.image = UIImage(named: "appcoda-300")
            
        } else {
            cell.postImageView.image = UIImage(named: "webkit-featured")
            cell.postTitleLabel.text = "Inauguración de la Universidad Modelo Chetumal"
            cell.authorLabel.text = "Universidad Modelo"
            cell.authorImageView.image = UIImage(named: "appcoda-300")
            
        }

        return cell
    }
    

